// 1) Primitive Datatypes in JavaScript     :- string, Number, Boolean, undefined, null, BigInt, Symbol
// 2) Non-Primitive Datatypes in JavaScript :- object




// 1) string

var a = "Tejas Joshi";
console.log(typeof a);  // string



// 2) Number

let m = 45;
console.log(typeof m);  // number
m = 23.32;
console.log(typeof m);  // number




// 3) Boolean

let p = true;
console.log(typeof p);  // boolean
p = false;
console.log(typeof p);  // boolean





// 4) undefined

let k = undefined;
console.log(typeof k);  // undefined