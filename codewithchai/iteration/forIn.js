const myObject = {
    js : "javascript",
    ts : "typescript",
    cpp : "C++",
    py : "python"
}


for (const key in myObject) {
    console.log(`${key}`);
}
// js
// ts
// cpp
// py


for (const key in myObject) {
    console.log(`${key} : ${myObject[key]}`)
}
// js : javascript
// ts : typescript
// cpp : C++
// py : python


